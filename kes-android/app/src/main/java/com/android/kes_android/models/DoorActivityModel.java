package com.android.kes_android.models;

public class DoorActivityModel {

    private String mAdmin;
    private String mProfileType;
    private String mUsername;
    private String mPhotoSimplename;
    private String mGranted;
    private String mID;

    public DoorActivityModel() {}

    public String getAdmin() {
        return mAdmin;
    }

    public void setAdmin(String admin) {
        mAdmin = admin;
    }

    public String getProfileType() {
        return mProfileType;
    }

    public void setProfileType(String profileType) {
        mProfileType = profileType;
    }

    public String getUsername() {
        return mUsername;
    }

    public void setUsername(String username) {
        mUsername = username;
    }

    public String getPhotoSimplename() {
        return mPhotoSimplename;
    }

    public void setPhotoSimplename(String photoSimplename) {
        mPhotoSimplename = photoSimplename;
    }

    public String getGranted() {
        return mGranted;
    }

    public void setGranted(String granted) {
        mGranted = granted;
    }

    public String getID() {
        return mID;
    }

    public void setID(String ID) {
        mID = ID;
    }

}
